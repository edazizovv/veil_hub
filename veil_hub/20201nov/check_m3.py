def preprocessing(data, kwargs):

    data_ = data.copy()
    data_ = data_.set_index(['Timestamp'])
    data_ = data_.sort_index(ascending=True)

    cols = data_.columns.values

    for j in range(kwargs['n_lags'] + 2):
        data_[[x + '_LAG{0}'.format(j) for x in cols]] = data_[cols].shift(periods=j)
    data_ = data_.iloc[kwargs['n_lags'] + 1:, :].copy()

    return data_


def pipe_line(train, val, kwargs):

    train_ = train.copy()
    val_ = val.copy()

    cols = numpy.unique([x[:x.index('_LAG')] if '_LAG' in x else x for x in train_.columns.values])

    for j in range(kwargs['n_lags'] + 1):
        for col in cols:
            train_[col + '_LAG{0}'.format(j) + '_lnpct'] = log_verse(train_[col + '_LAG{0}'.format(j)].values, train_[col + '_LAG{0}'.format(j + 1)].values)
            val_[col + '_LAG{0}'.format(j) + '_lnpct'] = log_verse(val_[col + '_LAG{0}'.format(j)].values, val_[col + '_LAG{0}'.format(j + 1)].values)

    target = 'BTC-USD_Close||quantile_0.5_LAG0_lnpct'
    target_base = 'BTC-USD_Close||quantile_0.5_LAG1'
    factors = [x for x in train_.columns.values if 'LAG0' not in x and '_lnpct' in x]

    x_train = train_[factors].values
    y_train = train_[target].values
    x_val = val_[factors].values
    y_val = val_[target].values

    c0 = make_pipeline(*kwargs['dt_def_args'])
    c0.fit(X=x_train, y=y_train.flatten())

    c1 = make_pipeline(*kwargs['rf_def_args'])
    c1.fit(X=x_train, y=y_train.flatten())

    c2 = make_pipeline(*kwargs['rt_def_args'])
    c2.fit(X=x_train, y=y_train.flatten())

    c3 = make_pipeline(*kwargs['xb_def_args'])
    c3.fit(X=x_train, y=y_train.flatten())

    c4 = make_pipeline(*kwargs['cb_def_args'])
    c4.fit(X=x_train, y=y_train.flatten())

    c5 = make_pipeline(*kwargs['lb_def_args'])
    c5.fit(X=x_train, y=y_train.flatten())

    c6 = make_pipeline(*kwargs['gb_def_args'])
    c6.fit(X=x_train, y=y_train.flatten())

    c7 = make_pipeline(*kwargs['xblin_def_args'])
    c7.fit(X=x_train, y=y_train.flatten())

    c8 = make_pipeline(*kwargs['ls_def_args'])
    c8.fit(X=x_train, y=y_train.flatten())

    c9 = make_pipeline(*kwargs['sv_def_args'])
    c9.fit(X=x_train, y=y_train.flatten())

    c10 = make_pipeline(*kwargs['kn_def_args'])
    c10.fit(X=x_train, y=y_train.flatten())

    c_train = numpy.concatenate((c0.predict(x_train).reshape(-1, 1),
                                 c1.predict(x_train).reshape(-1, 1),
                                 c2.predict(x_train).reshape(-1, 1),
                                 c3.predict(x_train).reshape(-1, 1),
                                 c4.predict(x_train).reshape(-1, 1),
                                 c5.predict(x_train).reshape(-1, 1),
                                 c6.predict(x_train).reshape(-1, 1),
                                 c7.predict(x_train).reshape(-1, 1),
                                 c8.predict(x_train).reshape(-1, 1),
                                 c9.predict(x_train).reshape(-1, 1),
                                 c10.predict(x_train).reshape(-1, 1),), axis=1)
    c_val = numpy.concatenate((c0.predict(x_val).reshape(-1, 1),
                               c1.predict(x_val).reshape(-1, 1),
                               c2.predict(x_val).reshape(-1, 1),
                               c3.predict(x_val).reshape(-1, 1),
                               c4.predict(x_val).reshape(-1, 1),
                               c5.predict(x_val).reshape(-1, 1),
                               c6.predict(x_val).reshape(-1, 1),
                               c7.predict(x_val).reshape(-1, 1),
                               c8.predict(x_val).reshape(-1, 1),
                               c9.predict(x_val).reshape(-1, 1),
                               c10.predict(x_val).reshape(-1, 1),), axis=1)

    model = RandomForestRegressor()

    model.fit(X=c_train, y=y_train.ravel())

    yy_train_hat = model.predict(X=c_train)
    yy_val_hat = model.predict(X=c_val)

    y_train_bench = log_inverse(y_train.flatten(),
                                train_[target_base].values)
    y_train_hat = log_inverse(yy_train_hat.flatten(),
                              train_[target_base].values)
    y_val_bench = log_inverse(y_val.flatten(),
                              val_[target_base].values)
    y_val_hat = log_inverse(yy_val_hat.flatten(),
                            val_[target_base].values)

    return y_train_bench, y_train_hat, y_val_bench, y_val_hat


def _SMAPE(y_true, y_pred):
    value = (numpy.abs(y_true - y_pred) / ((y_true + y_pred) / 2)).sum() / y_true.shape[0]
    return value


def measures():
    return [r2_score, _SMAPE]



